    private long totalJual = 0, totalBeli = 0, totalLaba = 0;
    private int totalData = 0;
    Date DateNow = new Date(System.currentTimeMillis());

    private void tampilkanHasilPerhitungan(){
    NumberFormat rupiah = NumberFormat.getCurrencyInstance(new Locale("id", "ID"));

    // Jumlah (tidak perlu format Rupiah, misal ini count saja)
    lblJumlah.setText(String.valueOf(totalData));

    // Beli, Jual, dan Laba pakai format Rupiah
    lblBeli.setText(rupiah.format(totalBeli));
    lblJual.setText(rupiah.format(totalJual));

    long totalLaba = totalJual - totalBeli;
    lblLaba.setText(rupiah.format(totalLaba));
    
    }
    
    private void tampilkanSalesOrder(Date fromDate, Date toDate, Integer status) {
    // 1) Ambil data berdasar rentang tanggal dan status
    List<SalesOrder> daftar = SalesOrder.getSalesOrderToLaporan(fromDate, toDate, status);

    // 2) Siapkan kolom untuk JTable
    String[] kolom = {
        "No", "Nomor SO", "Tanggal", "Asal", "Tujuan",
        "Customer", "Supplier",  "Status"
    };
    DefaultTableModel model = new DefaultTableModel(kolom, 0) {
        @Override
        public boolean isCellEditable(int row, int column) {
            return false;
        }
    };
    
        // Hitung total berdasarkan field hargaTotal di SO
//        long totalSO = so.getHargaTotal();

        // Hitung total harga jual berdasarkan detailOrders:
        
    
    // 3) Isi baris per SalesOrder
    int no = 1;
    for (SalesOrder so : daftar) {
        
        
        String statusText = so.status == 0 ? "Belum Selesai" : "Selesai";
        model.addRow(new Object[]{
            no++,
            so.noSO,
            so.tanggal,
            so.dikirimDari,
            so.tujuan,
      so.getCustomers().isEmpty() ? "-" : so.getCustomers().get(0).nama,
      so.getSuppliers().isEmpty() ? "-" : so.getSuppliers().get(0).nama,
            statusText
            
        });
        totalData = no;
        for (DetailOrder det : so.getDetailOrders()) {
            totalBeli += det.kuantitas * det.hargaBeli;
            totalJual += det.kuantitas * det.hargaJual;
            
        };
        
    }
        if(daftar.isEmpty()){
            totalBeli = 0;
            totalJual = 0;
            totalData = 0;
            totalBeli = 0;
            
        }
                       tampilkanHasilPerhitungan();


    // 4) Pasang model ke tabel, atur lebar kolom No
    tblSalesOrder.setModel(model);
    tblSalesOrder.getColumnModel().getColumn(0).setMinWidth(30);
    tblSalesOrder.getColumnModel().getColumn(0).setMaxWidth(30);
}

    
