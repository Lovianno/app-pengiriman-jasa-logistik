    private void btnCariLaporanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCariLaporanActionPerformed
        // TODO add your handling code here:
        totalJual = 0;
                totalBeli = 0;
                
         java.util.Calendar cal = java.util.Calendar.getInstance();
    java.util.Date utilFrom, utilTo;

    switch (cbSorting.getSelectedIndex()) {
        case 0: // Harian: pakai kedua date chooser
            utilFrom = txtTanggalAwal.getDate();
            utilTo   = txtTanggalAkhir.getDate();
            break;

        case 1: // Bulanan: tanggal awal = hari pertama bulan, tanggal akhir = hari terakhir bulan
            utilFrom = txtTanggalAwal.getDate();
            cal.setTime(utilFrom);
            cal.set(java.util.Calendar.DAY_OF_MONTH, 1);
            utilFrom = cal.getTime();
            cal.set(java.util.Calendar.DAY_OF_MONTH, cal.getActualMaximum(java.util.Calendar.DAY_OF_MONTH));
            utilTo   = cal.getTime();
            break;

        case 2: // Tahunan: tanggal awal = 1 Jan, tanggal akhir = 31 Des tahun yang dipilih
            utilFrom = txtTanggalAwal.getDate();
            cal.setTime(utilFrom);
            int year = cal.get(java.util.Calendar.YEAR);
            cal.set(year, java.util.Calendar.JANUARY,   1);
            utilFrom = cal.getTime();
            cal.set(year, java.util.Calendar.DECEMBER, 31);
            utilTo   = cal.getTime();
            break;

        default:
            utilFrom = txtTanggalAwal.getDate();
            utilTo   = txtTanggalAkhir.getDate();
    }

    // Konversi ke java.sql.Date
    Date fromDate = new Date(utilFrom.getTime());
    Date toDate   = new Date(utilTo.getTime());

    // Panggil DAO dengan rentang dan status (null = semua status)
    tampilkanSalesOrder(fromDate, toDate, 1);
  

    }//GEN-LAST:event_btnCariLaporanActionPerformed

