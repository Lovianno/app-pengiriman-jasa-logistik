     private void tampilkanInvoice(String cari) {
    List<Invoice> daftar = Invoice.getDataInvoiceWithStatus(cari, null);
    String[] kolom = {"No", "No Invoice", "Nomor SO",  "Tanggal", "Jatuh Tempo"};

    // Buat model tabel yang tidak bisa diedit
    DefaultTableModel model = new DefaultTableModel(kolom, 0) {
        @Override
        public boolean isCellEditable(int row, int column) {
            return false;
        }
    };

    int i = 0;
    for (Invoice inv : daftar) {
        Object[] row = {
            ++i,
            inv.noInvoice,
            inv.noSO,
            inv.tanggalInvoice,
            inv.tanggalJatuhTempo   
        };
        model.addRow(row);
    }

    tblInvoice.setModel(model); // set model baru ke JTable

    // Lebar kolom 'No' dibatasi agar kecil
    tblInvoice.getColumnModel().getColumn(0).setMinWidth(30);
    tblInvoice.getColumnModel().getColumn(0).setMaxWidth(30);
    tblInvoice.getColumnModel().getColumn(0).setWidth(30);
}
    
