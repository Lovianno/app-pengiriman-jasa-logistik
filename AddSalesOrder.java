    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
     DefaultTableModel model = (DefaultTableModel) tblCart.getModel();

      if (model.getRowCount() == 0) {
           JOptionPane.showMessageDialog(null, "Produk tidak boleh kosong");
           return;
       }
     if(txtAsal.getText().trim().isEmpty()){
           JOptionPane.showMessageDialog(null, "Asal Pengiriman tidak boleh kosong");
                               return;

     }
     if(txtTujuan.getText().trim().isEmpty()){
                    JOptionPane.showMessageDialog(null, "Tujuan Pengiriman tidak boleh kosong");
                               return;

     }
        String NoSO = SalesOrder.generateNoSO(4);
      Date tanggalSekarang = new Date(System.currentTimeMillis());
       SalesOrder so = new SalesOrder(NoSO,tanggalSekarang ,txtAsal.getText(),  txtTujuan.getText(), idCustomer, idSupplier, Login.idPegawai
 , totalHargaJual, 0);
       so.createSO();

    for (int i = 0; i < model.getRowCount(); i++) {
        int produkId = Integer.parseInt(model.getValueAt(i, 1).toString());
        String deskripsi = model.getValueAt(i, 7).toString(); // asumsi kolom ke-7 = deskripsi
        int kuantitas = Integer.parseInt(model.getValueAt(i, 4).toString());
        long hargaBeli = Long.parseLong(model.getValueAt(i, 5).toString());
        long hargaJual = Long.parseLong(model.getValueAt(i, 6).toString());

        DetailOrder detail = new DetailOrder(NoSO, produkId, deskripsi, kuantitas, hargaBeli, hargaJual);
        detail.createDetailSO();
    }
       JOptionPane.showMessageDialog(this, "Sales Order Berhasil dibuat", "Info", JOptionPane.INFORMATION_MESSAGE);
       
        String formatNoInvoice= so.noSO;
        Date sqlTanggal = tanggalSekarang;
        Invoice inv = new Invoice("INV-"+formatNoInvoice.substring(formatNoInvoice.length()-4), so.noSO, sqlTanggal, null);
        inv.createInvoice();
        
        MenuSalesOrder mso = new MenuSalesOrder();
        mso.setVisible(true);
        dispose();
    }//GEN-LAST:event_jButton2ActionPerformed

