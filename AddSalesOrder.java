    private void tblCartMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblCartMouseClicked
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) tblCart.getModel();
        int selectedRow = tblCart.getSelectedRow();

         if (selectedRow != -1) { // pastikan ada yang dipilih
    //    idProduk = model.getValueAt(selectedIndexRow, 1).toString());
//          cbProduk.setSelectedItem(model.getValueAt(selectedRow, 2).toString());
          String nama = model.getValueAt(selectedRow, 2).toString();
         for (int i = 0; i < cbProduk.getItemCount(); i++) {
         Produk p = (Produk) cbProduk.getItemAt(i);
        if (p.nama.equals(nama)) {
        cbProduk.setSelectedIndex(i);
        break;
        }
    }
            txtKategori.setText(model.getValueAt(selectedRow, 3).toString());
            txtQty.setValue(model.getValueAt(selectedRow, 4));
            txtHargaBeli.setValue(model.getValueAt(selectedRow, 5));
            txtHargaJual.setValue(model.getValueAt(selectedRow, 6));
            txtDeskripsi.setText(model.getValueAt(selectedRow, 7).toString());

    }


    }//GEN-LAST:event_tblCartMouseClicked

